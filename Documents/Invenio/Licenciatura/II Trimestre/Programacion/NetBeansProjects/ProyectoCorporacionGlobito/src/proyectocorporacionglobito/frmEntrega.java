    private void bntBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntBuscarActionPerformed
        try {
            int id = Integer.parseInt(txtIdentificacion.getText());
            Vendedora vendedora = Global.listaVendedora.buscarVendedora(id);
            if (vendedora == null) {
                limpiarTabla();
                JOptionPane.showMessageDialog(rootPane, "Vendedora no existe!!!", "Vendedora",
                        JOptionPane.ERROR_MESSAGE);
            } else {
                lblNombre.setText(vendedora.getNombre());
                tblPedidos.setModel(Global.listaPedido.imprimirTabla(id));
                int total = 0;
                double totalPedido = 0.0;
                for (int i = 0; i < tblPedidos.getRowCount(); i++) {
                    total += Integer.parseInt(tblPedidos.getValueAt(i, 2).toString());
                    totalPedido += Double.parseDouble(tblPedidos.getValueAt(i, 4).toString());
                }
                lblTotalCantidad.setText("" + total);
                lblTotal.setText("" + totalPedido);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "El id de la vendedora debe ser un numero", "Vendedora",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_bntBuscarActionPerformed

    private void btnEntregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntregarActionPerformed
        try {
            int id = Integer.parseInt(txtIdentificacion.getText());
            Vendedora vendedora = Global.listaVendedora.buscarVendedora(id);
            if (vendedora != null) {
                Global.listaPedido.entregarPedido(id);
                Global.listaPedido.escribirPedidos();
                Global.listaEntrega.escribirEntregas();
                limpiarTabla();
                txtIdentificacion.setText("");
                JOptionPane.showMessageDialog(rootPane, "Pedido entregado exitosamente!!!", "Entrega",
                        JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Vendedora no existe!!!", "Vendedora",
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException exc) {
            JOptionPane.showMessageDialog(rootPane, "El id de la vendedora debe ser un numero", "Vendedora",
                    JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnEntregarActionPerformed

